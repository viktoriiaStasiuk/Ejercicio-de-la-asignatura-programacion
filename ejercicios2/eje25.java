public class eje25 {
  public static void main(String[] args) {
    int x = 144;
    System.out.println("El valor actual de mi variable es " + x);
    
    int y = 999;
    System.out.println("El valor actual de mi variable es " + y);

    int suma = x + y;
    System.out.println("La suma de mis variables es " + suma);
    
    int resta = x - y;
    System.out.println("El resto de mis variables es " + resta);
    
    double division;
    division = (double)x / (double)y;
    System.out.println("La division de mis variables es " + division);
    
    int mul = x * y;
    System.out.println("La multiplicación de mis variables es " + mul);  
  }
}
