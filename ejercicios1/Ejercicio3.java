public class Ejercicio3 {
  public static void main(String[] args) {
    System.out.printf("%-10s %s\n", "nature", "naturaleza");
    System.out.printf("%-10s %s\n", "girl", "chica");
    System.out.printf("%-10s %s\n", "book", "libro");
    System.out.printf("%-10s %s\n", "cat", "gato");
    System.out.printf("%-10s %s\n", "red", "rojo");
    System.out.printf("%-10s %s\n", "exercise", "ejercicio");
    System.out.printf("%-10s %s\n", "city", "ciudad");
    System.out.printf("%-10s %s\n", "sky", "cielo");
    System.out.printf("%-10s %s\n", "phone", "teléfino");
    System.out.printf("%-10s %s\n", "boy", "chico");
  }
}
